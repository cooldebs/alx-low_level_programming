#include "main.h"

/**
 * add - Checks the addition of two integers
 * @a: first integer.
 * @b: second integer.
 * Return: int.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Checks the subtraction of two integers
 * @a: first integer.
 * @b: second integer.
 * Return: int.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Checks the multiplication of two integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Checks the division of two integers.
 * @a: first integer.
 * @b: second integer.
 * Return: int.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Checks for modulo division.
 * @a: first integer.
 * @b: second integer.
 * Return: int.
 */
int mod(int a, int b)
{
	return (a % b);
}
